/// <summary>
/// Implements a CRDT strategy for collections that are treated as sorted sets. It uses the Longest Common Subsequence (LCS) algorithm to find differences and ensures the collection remains sorted after operations.
/// This strategy assumes elements can be uniquely identified and compared. Sorting can be configured via the <see cref="CrdtSortedSetStrategyAttribute.SortPropertyName"/>.
/// </summary>
[CrdtSupportedType(typeof(IList))]
[CrdtSupportedIntent(typeof(AddIntent))]
[CrdtSupportedIntent(typeof(RemoveValueIntent))]
[Commutative]
[Associative]
[Idempotent]
[OperationBased]
public sealed class SortedSetStrategy(
    IElementComparerProvider comparerProvider, 
    ReplicaContext replicaContext) : IPartitionableCrdtStrategy
{
    private readonly string replicaId = replicaContext.ReplicaId;

    /// <summary>
    /// Defines the type of difference operation in a Longest Common Subsequence (LCS) comparison.
    /// </summary>
    public enum LcsDiffEntryType { 
        /// <summary>
        /// Indicates that an element was found in both the original and modified collections.
        /// </summary>
        Match,
        /// <summary>
        /// Indicates that an element was added to the modified collection.
        /// </summary>
        Add,
        /// <summary>
        /// Indicates that an element was removed from the original collection.
        /// </summary>
        Remove
    }

    /// <summary>
    /// Represents a single entry in the difference list generated by the LCS algorithm, detailing a match, addition, or removal.
    /// </summary>
    /// <param name="Type">The type of the difference operation (<see cref="LcsDiffEntryType.Match"/>, <see cref="LcsDiffEntryType.Add"/>, or <see cref="LcsDiffEntryType.Remove"/>).</param>
    /// <param name="OldIndex">The index of the element in the original collection. This is -1 for an <see cref="LcsDiffEntryType.Add"/> operation.</param>
    /// <param name="NewIndex">The index of the element in the modified collection. This is -1 for a <see cref="LcsDiffEntryType.Remove"/> operation.</param>
    public readonly record struct LcsDiffEntry(LcsDiffEntryType Type, int OldIndex, int NewIndex);
    
    /// <inheritdoc/>
    public void GeneratePatch(GeneratePatchContext context)
    {
        var (operations, nestedDiffs, path, property, originalValue, modifiedValue, originalRoot, modifiedRoot, originalMeta, changeTimestamp) = context;

        var fromArray = (originalValue as IEnumerable)?.Cast<object>().ToList() ?? new List<object>();
        var toArray = (modifiedValue as IEnumerable)?.Cast<object>().ToList() ?? new List<object>();

        var elementType = PocoPathHelper.GetCollectionElementType(property);
        var comparer = comparerProvider.GetComparer(elementType);
        
        var diff = Diff(fromArray, toArray, comparer);

        var removeOps = new List<CrdtOperation>();
        var addOps = new List<CrdtOperation>();

        foreach (var entry in diff)
        {
            if (entry.Type == LcsDiffEntryType.Match)
            {
                var fromItem = fromArray[entry.OldIndex];
                var toItem = toArray[entry.NewIndex];
                
                if (!Equals(fromItem, toItem))
                {
                    var itemPath = $"{path}[{entry.OldIndex}]";
                    var diffContext = new DifferentiateObjectContext(itemPath, elementType, fromItem, toItem, originalRoot, modifiedRoot, originalMeta, operations, changeTimestamp);
                    nestedDiffs.Add(diffContext);
                }
            }
            else if (entry.Type == LcsDiffEntryType.Add)
            {
                var addPath = $"{path}[{entry.NewIndex}]";
                var addedItem = toArray[entry.NewIndex];
                addOps.Add(new CrdtOperation(Guid.NewGuid(), replicaId, addPath, OperationType.Upsert, addedItem, changeTimestamp));
            }
            else if (entry.Type == LcsDiffEntryType.Remove)
            {
                var removePath = $"{path}[{entry.OldIndex}]";
                var removedItem = fromArray[entry.OldIndex];
                removeOps.Add(new CrdtOperation(Guid.NewGuid(), replicaId, removePath, OperationType.Remove, removedItem, changeTimestamp));
            }
        }

        operations.AddRange(removeOps);
        operations.AddRange(addOps);
    }

    /// <inheritdoc/>
    public CrdtOperation GenerateOperation(GenerateOperationContext context)
    {
        var (_, _, jsonPath, _, intent, timestamp, opReplicaId) = context;

        // A dummy index like `[-1]` is used since ApplyOperation solely extracts the path up to `[` 
        // and manages elements based on the value mapping in the set.
        return intent switch
        {
            AddIntent add => new CrdtOperation(Guid.NewGuid(), opReplicaId, $"{jsonPath}[-1]", OperationType.Upsert, add.Value, timestamp),
            RemoveValueIntent remove => new CrdtOperation(Guid.NewGuid(), opReplicaId, $"{jsonPath}[-1]", OperationType.Remove, remove.Value, timestamp),
            _ => throw new NotSupportedException($"Intent {intent.GetType().Name} is not supported by {nameof(SortedSetStrategy)}.")
        };
    }
    
    /// <inheritdoc/>
    public void ApplyOperation(ApplyOperationContext context)
    {
        var (root, metadata, operation) = context;

        var lastBracket = operation.JsonPath.LastIndexOf('[');
        if (lastBracket <= 0 || !operation.JsonPath.EndsWith("]"))
        {
            return;
        }

        var collectionPath = operation.JsonPath[..lastBracket];
        var (parent, property, _) = PocoPathHelper.ResolvePath(root, collectionPath);

        if (parent is null || property is null)
        {
            return;
        }

        if (PocoPathHelper.GetAccessor(property).Getter(parent) is not IList list)
        {
            return;
        }

        var elementType = PocoPathHelper.GetCollectionElementType(property);
        var comparer = comparerProvider.GetComparer(elementType);

        if (operation.Type == OperationType.Upsert)
        {
            var newValue = PocoPathHelper.ConvertValue(operation.Value, elementType);
            if (newValue is null) return;

            var existingIndex = -1;
            for (var i = 0; i < list.Count; i++)
            {
                if (comparer.Equals(list[i], newValue))
                {
                    existingIndex = i;
                    break;
                }
            }

            if (existingIndex != -1)
            {
                list[existingIndex] = newValue;
            }
            else
            {
                list.Add(newValue);
            }

            SortList(list, property);
        }
        else if (operation.Type == OperationType.Remove)
        {
            if (operation.Value is null) return;
            var valueToRemove = PocoPathHelper.ConvertValue(operation.Value, elementType);
            if (valueToRemove is null) return;

            var indexToRemove = -1;
            for (var i = 0; i < list.Count; i++)
            {
                if (comparer.Equals(list[i], valueToRemove))
                {
                    indexToRemove = i;
                    break;
                }
            }

            if (indexToRemove != -1)
            {
                list.RemoveAt(indexToRemove);
            }
        }
    }

    /// <inheritdoc/>
    public IComparable? GetStartKey(object data, PropertyInfo partitionableProperty)
    {
        var list = PocoPathHelper.GetAccessor(partitionableProperty).Getter(data) as IList;
        if (list == null || list.Count == 0) return null;

        var attr = partitionableProperty.GetCustomAttribute<CrdtSortedSetStrategyAttribute>();
        var sortPropName = attr?.SortPropertyName;

        var keys = new List<IComparable>();
        foreach (var item in list)
        {
            var key = GetSortKey(item, sortPropName);
            if (key is IComparable c) keys.Add(c);
            else if (key != null) keys.Add(key.ToString()!);
        }

        return keys.OrderBy(k => k).FirstOrDefault();
    }

    /// <inheritdoc/>
    public IComparable? GetKeyFromOperation(CrdtOperation operation, string partitionablePropertyPath)
    {
        if (!operation.JsonPath.StartsWith(partitionablePropertyPath, StringComparison.Ordinal)) return null;

        // Note: For elements, GetSortKey will inherently try to parse "Id" or use the element itself.
        var sortKey = GetSortKey(operation.Value, null);
        return sortKey as IComparable ?? sortKey?.ToString() as IComparable;
    }

    /// <inheritdoc/>
    public IComparable GetMinimumKey(PropertyInfo partitionableProperty)
    {
        var elementType = PocoPathHelper.GetCollectionElementType(partitionableProperty);
        var attr = partitionableProperty.GetCustomAttribute<CrdtSortedSetStrategyAttribute>();
        var sortPropName = attr?.SortPropertyName ?? "Id";
        
        var prop = elementType.GetProperty(sortPropName, BindingFlags.Public | BindingFlags.Instance | BindingFlags.IgnoreCase);
        if (prop != null)
        {
            return GetMinimumKeyForType(prop.PropertyType);
        }

        return string.Empty;
    }

    /// <inheritdoc/>
    public SplitResult Split(object originalData, CrdtMetadata originalMetadata, PropertyInfo partitionableProperty)
    {
        var documentType = partitionableProperty.DeclaringType!;
        var path = $"$.{char.ToLowerInvariant(partitionableProperty.Name[0])}{partitionableProperty.Name[1..]}";

        var list = PocoPathHelper.GetAccessor(partitionableProperty).Getter(originalData) as IList;
        if (list == null || list.Count < 2)
        {
            throw new InvalidOperationException("Cannot split a partition with less than 2 items.");
        }

        var attr = partitionableProperty.GetCustomAttribute<CrdtSortedSetStrategyAttribute>();
        var sortPropName = attr?.SortPropertyName;

        var items = list.Cast<object>().Select(x => new { Item = x, SortKey = GetSortKey(x, sortPropName) as IComparable ?? GetSortKey(x, sortPropName)?.ToString() as IComparable })
            .Where(x => x.SortKey != null).OrderBy(x => x.SortKey).ToList();

        var splitIndex = items.Count / 2;
        var splitKey = items[splitIndex].SortKey;

        var keys1 = items.Take(splitIndex).Select(x => x.SortKey).ToHashSet();
        var keys2 = items.Skip(splitIndex).Select(x => x.SortKey).ToHashSet();

        var doc1 = Activator.CreateInstance(documentType)!;
        var doc2 = Activator.CreateInstance(documentType)!;

        var elementType = PocoPathHelper.GetCollectionElementType(partitionableProperty);

        ReconstructListForSplitMerge(doc1, path, list, keys1, sortPropName, elementType);
        ReconstructListForSplitMerge(doc2, path, list, keys2, sortPropName, elementType);

        return new SplitResult(new PartitionContent(doc1, originalMetadata.DeepClone()), new PartitionContent(doc2, originalMetadata.DeepClone()), splitKey!);
    }

    /// <inheritdoc/>
    public PartitionContent Merge(object data1, CrdtMetadata meta1, object data2, CrdtMetadata meta2, PropertyInfo partitionableProperty)
    {
        var documentType = partitionableProperty.DeclaringType!;
        var path = $"$.{char.ToLowerInvariant(partitionableProperty.Name[0])}{partitionableProperty.Name[1..]}";

        var mergedDoc = Activator.CreateInstance(documentType)!;
        var mergedMeta = CrdtMetadata.Merge(meta1, meta2);

        var list1 = PocoPathHelper.GetAccessor(partitionableProperty).Getter(data1) as IList;
        var list2 = PocoPathHelper.GetAccessor(partitionableProperty).Getter(data2) as IList;

        var (parent, property, _) = PocoPathHelper.ResolvePath(mergedDoc, path);
        if (parent is not null && property is not null)
        {
            var elementType = PocoPathHelper.GetCollectionElementType(partitionableProperty);
            var mergedList = (IList)Activator.CreateInstance(typeof(List<>).MakeGenericType(elementType))!;

            if (list1 != null) foreach (var item in list1) mergedList.Add(item);
            if (list2 != null) foreach (var item in list2) mergedList.Add(item);

            PocoPathHelper.GetAccessor(property).Setter(parent, mergedList);
            SortList(mergedList, partitionableProperty);
        }

        return new PartitionContent(mergedDoc, mergedMeta);
    }
    
    internal List<LcsDiffEntry> Diff(List<object> from, List<object> to, IEqualityComparer<object> itemComparer)
    {
        var n = from.Count;
        var m = to.Count;
        var dp = new int[n + 1, m + 1];

        for (var i = 1; i <= n; i++)
        {
            for (var j = 1; j <= m; j++)
            {
                if (itemComparer.Equals(from[i-1], to[j-1]))
                {
                    dp[i, j] = dp[i - 1, j - 1] + 1;
                }
                else
                {
                    dp[i, j] = Math.Max(dp[i - 1, j], dp[i, j - 1]);
                }
            }
        }

        var diff = new List<LcsDiffEntry>();
        var i_ptr = n;
        var j_ptr = m;
        while (i_ptr > 0 || j_ptr > 0)
        {
            if (i_ptr > 0 && j_ptr > 0 && itemComparer.Equals(from[i_ptr - 1], to[j_ptr - 1]))
            {
                diff.Add(new LcsDiffEntry(LcsDiffEntryType.Match, i_ptr - 1, j_ptr - 1));
                i_ptr--;
                j_ptr--;
            }
            else if (j_ptr > 0 && (i_ptr == 0 || dp[i_ptr, j_ptr - 1] >= dp[i_ptr - 1, j_ptr]))
            {
                diff.Add(new LcsDiffEntry(LcsDiffEntryType.Add, -1, j_ptr - 1));
                j_ptr--;
            }
            else if (i_ptr > 0 && (j_ptr == 0 || dp[i_ptr, j_ptr - 1] < dp[i_ptr - 1, j_ptr]))
            {
                diff.Add(new LcsDiffEntry(LcsDiffEntryType.Remove, i_ptr - 1, -1));
                i_ptr--;
            }
        }

        diff.Reverse();
        return diff;
    }
    
    private static void SortList(IList list, PropertyInfo property)
    {
        var attr = property.GetCustomAttribute<CrdtSortedSetStrategyAttribute>();
        var sortPropertyName = attr?.SortPropertyName;

        var items = list.Cast<object>().ToList();

        items.Sort((x, y) =>
        {
            var keyX = GetSortKey(x, sortPropertyName);
            var keyY = GetSortKey(y, sortPropertyName);
            
            if (keyX is null && keyY is null) return 0;
            if (keyX is null) return -1;
            if (keyY is null) return 1;

            if (keyX is IComparable comparableX)
            {
                return comparableX.CompareTo(keyY);
            }

            return string.Compare(keyX.ToString(), keyY.ToString(), StringComparison.Ordinal);
        });
        
        list.Clear();
        foreach (var item in items)
        {
            list.Add(item);
        }
    }

    private static void ReconstructListForSplitMerge(object root, string path, IList sourceList, HashSet<IComparable?> keysToKeep, string? sortPropName, Type elementType)
    {
        var (parent, property, _) = PocoPathHelper.ResolvePath(root, path);
        if (parent is null || property is null) return;

        var list = PocoPathHelper.GetAccessor(property).Getter(parent) as IList;
        if (list is null)
        {
            var listType = typeof(List<>).MakeGenericType(elementType);
            list = (IList)Activator.CreateInstance(listType)!;
            PocoPathHelper.GetAccessor(property).Setter(parent, list);
        }

        list.Clear();
        foreach (var item in sourceList)
        {
            var sortKey = GetSortKey(item, sortPropName) as IComparable ?? GetSortKey(item, sortPropName)?.ToString() as IComparable;
            if (keysToKeep.Contains(sortKey))
            {
                var converted = PocoPathHelper.ConvertValue(item, elementType);
                if (converted != null) list.Add(converted);
            }
        }
    }

    private static object? GetSortKey(object? obj, string? sortPropertyName)
    {
        if (obj is null)
        {
            return null;
        }

        var type = obj.GetType();
        PropertyInfo? prop = null;

        if (!string.IsNullOrEmpty(sortPropertyName))
        {
            prop = type.GetProperty(sortPropertyName, BindingFlags.Public | BindingFlags.Instance | BindingFlags.IgnoreCase);
        }
        else
        {
            prop = type.GetProperty("Id", BindingFlags.Public | BindingFlags.Instance | BindingFlags.IgnoreCase);
        }
        
        if (prop is not null)
        {
            return PocoPathHelper.GetAccessor(prop).Getter(obj);
        }

        if (obj is IComparable)
        {
            return obj;
        }

        if (type.IsClass && type != typeof(string))
        {
            var properties = type.GetProperties(BindingFlags.Public | BindingFlags.Instance)
                .Where(p => p.CanRead)
                .OrderBy(p => p.Name);
            
            var keyBuilder = new StringBuilder();
            foreach (var p in properties)
            {
                keyBuilder.Append(PocoPathHelper.GetAccessor(p).Getter(obj)?.ToString() ?? "null");
                keyBuilder.Append('|');
            }
            return keyBuilder.ToString();
        }

        return obj;
    }

    private static IComparable GetMinimumKeyForType(Type keyType)
    {
        if (keyType == typeof(string)) return string.Empty;
        if (keyType == typeof(int)) return int.MinValue;
        if (keyType == typeof(long)) return long.MinValue;
        if (keyType == typeof(short)) return short.MinValue;
        if (keyType == typeof(byte)) return byte.MinValue;
        if (keyType == typeof(Guid)) return Guid.Empty;
        if (keyType == typeof(DateTime)) return DateTime.MinValue;
        if (keyType == typeof(DateTimeOffset)) return DateTimeOffset.MinValue;
        if (keyType == typeof(char)) return char.MinValue;
        if (keyType == typeof(double)) return double.MinValue;
        if (keyType == typeof(float)) return float.MinValue;
        if (keyType == typeof(decimal)) return decimal.MinValue;

        if (keyType.IsValueType)
        {
            return (IComparable)Activator.CreateInstance(keyType)!;
        }

        throw new InvalidOperationException($"Cannot determine minimum key for type {keyType}.");
    }
}