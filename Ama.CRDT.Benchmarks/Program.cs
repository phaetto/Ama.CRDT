namespace Ama.CRDT.Benchmarks;

using System;
using System.IO;
using System.Linq;
using System.Text;
using BenchmarkDotNet.Running;

/// <summary>
/// Main entry point for the benchmark application.
/// </summary>
public static class Program
{
    /// <summary>
    /// Runs all benchmarks defined in this assembly.
    /// </summary>
    /// <param name="args">Command-line arguments passed to BenchmarkDotNet.</param>
    public static void Main(string[] args)
    {
        // Run benchmarks
        BenchmarkSwitcher.FromAssembly(typeof(Program).Assembly).Run(args);

        // Automatically update the README.md with the latest results
        UpdateReadme();
    }

    private static void UpdateReadme()
    {
        var projectRoot = GetProjectRoot();
        if (projectRoot is null)
        {
            Console.WriteLine("Could not find project root to update README.md");
            return;
        }

        var readmePath = Path.Combine(projectRoot, "README.md");
        
        // BenchmarkDotNet generates results in the 'BenchmarkDotNet.Artifacts' directory relative to where it was executed.
        var artifactsPath = Path.Combine(Environment.CurrentDirectory, "BenchmarkDotNet.Artifacts", "results");

        if (!Directory.Exists(artifactsPath))
        {
            // Fallback in case the execution directory wasn't the bin output
            artifactsPath = Path.Combine(projectRoot, "BenchmarkDotNet.Artifacts", "results");
        }

        if (!Directory.Exists(artifactsPath))
        {
            Console.WriteLine($"Could not find Benchmark artifacts folder at {artifactsPath}");
            return;
        }

        // We specifically look for the GitHub Markdown export generated by BenchmarkDotNet
        var reportFiles = Directory.GetFiles(artifactsPath, "*-github.md");
        if (reportFiles.Length == 0)
        {
            Console.WriteLine("No github markdown reports found in artifacts directory.");
            return;
        }

        var sb = new StringBuilder();
        foreach (var file in reportFiles)
        {
            sb.AppendLine(File.ReadAllText(file));
            sb.AppendLine();
        }

        var startMarker = "<!-- BENCHMARKS_START -->";
        var endMarker = "<!-- BENCHMARKS_END -->";

        if (!File.Exists(readmePath))
        {
            File.WriteAllText(readmePath, $"{startMarker}\n{endMarker}\n");
        }

        var readmeContent = File.ReadAllText(readmePath);
        var startIndex = readmeContent.IndexOf(startMarker, StringComparison.Ordinal);
        var endIndex = readmeContent.IndexOf(endMarker, StringComparison.Ordinal);

        if (startIndex >= 0 && endIndex >= 0 && endIndex > startIndex)
        {
            var newReadme = readmeContent.Substring(0, startIndex + startMarker.Length) +
                            "\n" + sb.ToString() +
                            readmeContent.Substring(endIndex);
            
            File.WriteAllText(readmePath, newReadme);
            Console.WriteLine("Successfully updated README.md with benchmark results.");
        }
        else
        {
            // If markers don't exist, append them to the end of the file
            File.AppendAllText(readmePath, $"\n{startMarker}\n{sb}{endMarker}\n");
            Console.WriteLine("Appended benchmark results and markers to README.md.");
        }
    }

    private static string? GetProjectRoot()
    {
        var dir = new DirectoryInfo(Environment.CurrentDirectory);
        while (dir != null)
        {
            if (dir.GetFiles("Ama.CRDT.Benchmarks.csproj").Any())
            {
                return dir.FullName;
            }
            dir = dir.Parent;
        }
        return null;
    }
}